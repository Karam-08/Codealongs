let weather = 'rainy';
switch(weather){
    case'sunny':
        console.log("Wear Sunglasses.")
        break;
    case'rainy':
        console.log("Take an Umbrella today.")
        break;
    case'cloudy':
        console.log("You might need a light jacket.")
        break;
    case'snowy':
        console.log("You should wear a coat and gloves today.")
        break;
    default:
        console.log("The weather is unpredictable, I guess.")
}

